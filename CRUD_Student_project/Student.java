package school;

public class Student {
 private String name;
 private int id;
 private String std;
public Student(String name, int id, String std) {
	this.name = name;
	this.id = id;
	this.std = std;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getStd() {
	return std;
}
public void setStd(String std) {
	this.std = std;
}

 
}
